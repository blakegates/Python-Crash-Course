foods = ('sausage', 'bacon', 'ham', 'cheese', 'eggs')

print("The restaurant offers these foods:\n")
for food in foods:
    print(food)

# Modifying a tuple with for loop
modified_foods = ('sausage', 'bacon', 'ham', 'mayonaise', 'lettuce')

print("The new menu consists of:\n")
for foods in modified_foods:
    print(foods)